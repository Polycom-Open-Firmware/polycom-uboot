/*
 * rtl8363nbvb.c - u-boot init driver for the Realtek RTL8363NB-VB
 *                 unmanaged switch as wired on Polycom TC8 / Trio C60.
 *
 * Clean-room re-implementation of the stock vendor bring-up, ported
 * from re/rtl83_switch_clean/rtl83_switch.c (Linux module) to u-boot
 * DM.  Brings the chip up in TRANSPARENT L2 forwarding mode (no DSA,
 * no CPU tag) then exits; the FEC drives a plain fixed-link 1G/full
 * RGMII MAC and the chip forwards CPU<->wire in HW.
 *
 *   u-boot <-> FEC eth0 <-> RGMII <-> port6 (extif1) <-> switch
 *                                                        <-> port1 <-> wire
 *
 * UNIVERSAL PATH: idempotent. On a unit where a prior boot stage
 * already configured the switch (HAB-closed TC8: stock signed u-boot
 * 2018.03 inits it then chainloads us), rtl8363_already_up() detects
 * the live transparent config and SKIPS the heavy reload — preserving
 * the proven TC8-bench parity. On a cold unit with no pre-init (C60
 * via uuu/SDP, or any stock-replaced flash) it runs the full 7-stage
 * sequence. DT reset-gpios present => cold-init that unit; absent =>
 * inherit/skip.
 *
 * Data tables (OCP patch + 8051 fw) are auto-extracted byte-for-byte;
 * see the AUTO-EXTRACTED banner below. Sources of truth:
 *   re/patch_chip_data.c            (210-entry PHY-OCP, binary 0x11a9e98)
 *   re/rtl83_switch_clean/...       (1507-byte 8051 fw, regs 0xe000+)
 *   re/rtl83_funcs/_rtk_switch_init_8367c_*  (jam-table disasm)
 *   /tmp/stock_regs.txt             (live stock register ground truth)
 */

