/* ---- SDK port: port_admin_init (1507-byte 8051 fw -> 0xe000+) ---- */
static int rtl83_port_admin_init(void *dev, struct rtl8363_priv *p)
{
	u16 chip_ver;
	size_t i;
	int ret;

	dev_info(dev, "loading 8051 firmware (%u bytes)\n",
		 (unsigned)sizeof(rtl8363_8051_fw));
	RTL_DO(rtl83_smi_write(p, RTL_REG_RTL_MAGIC_UNLOCK,
			       RTL_MAGIC_UNLOCK_VAL));
	RTL_DO(rtl83_smi_read(p, RTL_REG_CHIP_VER, &chip_ver));
	if (chip_ver != 0x6367) {
		dev_err(dev, "unexpected chip ver 0x%04x (want 0x6367)\n",
			chip_ver);
		return -ENODEV;
	}
	RTL_DO(rtl83_smi_field_write(p, RTL_REG_PORTADM_GATE_B, BIT(4), BIT(4)));
	RTL_DO(rtl83_smi_field_write(p, RTL_REG_PORTADM_GATE_A, BIT(5), BIT(5)));
	RTL_DO(rtl83_smi_field_write(p, RTL_REG_PORTADM_GATE_C, BIT(1), BIT(1)));
	RTL_DO(rtl83_smi_field_write(p, RTL_REG_PORTADM_GATE_C, BIT(2), 0));
	for (i = 0; i < ARRAY_SIZE(rtl8363_8051_fw); i++) {
		ret = rtl83_smi_write(p, 0xe000 + i, rtl8363_8051_fw[i]);
		if (ret) {
			dev_err(dev, "fw byte %u failed (%d)\n",
				(unsigned)i, ret);
			return ret;
		}
	}
	RTL_DO(rtl83_smi_field_write(p, RTL_REG_PORTADM_GATE_C, BIT(1), BIT(1)));
	RTL_DO(rtl83_smi_field_write(p, RTL_REG_PORTADM_GATE_C, BIT(0), 0));
	RTL_DO(rtl83_smi_field_write(p, RTL_REG_PORTADM_GATE_B, BIT(4), 0));
	dev_info(dev, "8051 firmware loaded\n");
	return 0;
}

