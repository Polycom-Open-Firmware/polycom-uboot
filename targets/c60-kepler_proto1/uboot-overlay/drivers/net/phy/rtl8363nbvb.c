/* ---- chip identity: PHY1 reg2/3 direct on the FEC bus ------------- */
static int rtl83_detect(void *dev, struct rtl8363_priv *p)
{
	u16 h = 0, l = 0;
	int hi, lo;
	u32 oui;

	hi = miiphy_read(MIIBUS, 1, MII_PHYSID1, &h); hi = hi ? hi : h;
	lo = miiphy_read(MIIBUS, 1, MII_PHYSID2, &l); lo = lo ? lo : l;
	if (hi < 0 || lo < 0) {
		dev_err(dev, "PHY ID read failed (hi=%d lo=%d)\n", hi, lo);
		return hi < 0 ? hi : lo;
	}
	oui = ((u32)(hi & 0xffff) << 16) | (lo & 0xffff);
	dev_info(dev, "PHY1 OUI/model = 0x%08x\n", oui);
	if (oui != RTL_OUI_REALTEK) {
		dev_err(dev, "unexpected OUI 0x%08x (want 0x%08x)\n",
			oui, RTL_OUI_REALTEK);
		return -ENODEV;
	}
	return 0;
}

static void rtl83_chip_reset(void *dev, struct rtl8363_priv *p)
{
	/*
	 * No GPIO reset path (would need DM gpio + a DT reset-gpios).
	 * A power-on C60 chip is already at POR defaults; the full
	 * init programs it from there, and rtl83_already_up() handles
	 * the inherited-already-configured case. Kept as a no-op so
	 * the bringup sequence/structure matches the clean-room driver.
	 */
	(void)dev; (void)p;
}

/* ---- PHY OCP patch ritual + 210-entry table ---------------------- */
static int rtl83_phy_patch_ritual(void *dev,
				   struct rtl8363_priv *p, u8 phy)
{
	u16 v = 0;
	int ret, i;

	for (i = 0; i < 100; i++) {
		RTL_DO(rtl83_phy_ocp_read(p, phy, 0xa420, &v));
		if ((v & 0x7) == 3)
			break;
		udelay(150);
	}
	if ((v & 0x7) != 3)
		dev_warn(dev, "phy%u patch-ready didn't assert (0xa420=0x%04x)\n",
			 phy, v);

	RTL_DO(rtl83_phy_ocp_write(p, phy, 0xb83e, 0x6fa9));
	RTL_DO(rtl83_phy_ocp_write(p, phy, 0xb840, 0x00a9));
	RTL_DO(rtl83_phy_ocp_read(p, phy, 0xb820, &v));
	RTL_DO(rtl83_phy_ocp_write(p, phy, 0xb820, v | 0x10));
	for (i = 0; i < 20; i++) {
		RTL_DO(rtl83_phy_ocp_read(p, phy, 0xb800, &v));
		if (v & BIT(6))
			break;
		udelay(300);
	}
	return 0;
}

static int rtl83_apply_phy_patch(void *dev,
				 struct rtl8363_priv *p, u8 phy)
{
	int ret;
	size_t i;

	for (i = 0; i < ARRAY_SIZE(rtl8363_phy_ocp_patch); i++)
		RTL_DO(rtl83_phy_ocp_write(p, phy,
					   rtl8363_phy_ocp_patch[i][0],
					   rtl8363_phy_ocp_patch[i][1]));
	return 0;
}

