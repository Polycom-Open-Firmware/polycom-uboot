/* ---- MDIO indirect SMI primitives (legacy miiphy on "FEC" bus) ---- */
static int r83_mr(struct rtl8363_priv *p, int reg)
{
	u16 v = 0;
	int e = miiphy_read(MIIBUS, p->addr, reg, &v);

	return e ? e : (int)v;
}
static int r83_mw(struct rtl8363_priv *p, int reg, u16 v)
{
	return miiphy_write(MIIBUS, p->addr, reg, v);
}

static int rtl83_smi_write(struct rtl8363_priv *p, u16 reg, u16 val)
{
	int ret;

	ret = r83_mw(p, RTL_MDIO_CTRL0_REG, RTL_MDIO_ADDR_OP);
	if (ret)
		return ret;
	ret = r83_mw(p, RTL_MDIO_ADDRESS_REG, reg);
	if (ret)
		return ret;
	ret = r83_mw(p, RTL_MDIO_DATA_WRITE_REG, val);
	if (ret)
		return ret;
	return r83_mw(p, RTL_MDIO_CTRL1_REG, RTL_MDIO_WRITE_OP);
}

static int rtl83_smi_read(struct rtl8363_priv *p, u16 reg, u16 *val)
{
	int ret;

	ret = r83_mw(p, RTL_MDIO_CTRL0_REG, RTL_MDIO_ADDR_OP);
	if (ret)
		return ret;
	ret = r83_mw(p, RTL_MDIO_ADDRESS_REG, reg);
	if (ret)
		return ret;
	ret = r83_mw(p, RTL_MDIO_CTRL1_REG, RTL_MDIO_READ_OP);
	if (ret)
		return ret;
	ret = r83_mr(p, RTL_MDIO_DATA_READ_REG);
	if (ret < 0)
		return ret;
	*val = (u16)ret;
	return 0;
}

static int rtl83_smi_field_write(struct rtl8363_priv *p, u16 reg,
				 u16 mask, u16 val)
{
	u16 cur;
	int ret;

	ret = rtl83_smi_read(p, reg, &cur);
	if (ret)
		return ret;
	cur = (cur & ~mask) | (val & mask);
	return rtl83_smi_write(p, reg, cur);
}

/* ---- PHY OCP indirect access (setAsicPHYOCPReg) ------------------- */
static int rtl83_phy_ocp_write(struct rtl8363_priv *p, u8 phy,
			       u16 ocp_addr, u16 val)
{
	u16 data_reg;
	int ret;

	ret = rtl83_smi_field_write(p, RTL_REG_OCP_PREFIX, 0x0fc0,
				    (ocp_addr >> 10) << 6);
	if (ret)
		return ret;
	data_reg = ((u16)phy << 5) | 0x2000 |
		   ((ocp_addr >> 1) & 0x1f) |
		   (((ocp_addr >> 6) & 0x0f) << 8);
	return rtl83_smi_write(p, data_reg, val);
}

static int rtl83_phy_ocp_read(struct rtl8363_priv *p, u8 phy,
			      u16 ocp_addr, u16 *val)
{
	u16 data_reg;
	int ret;

	ret = rtl83_smi_field_write(p, RTL_REG_OCP_PREFIX, 0x0fc0,
				    (ocp_addr >> 10) << 6);
	if (ret)
		return ret;
	data_reg = ((u16)phy << 5) | 0x2000 |
		   ((ocp_addr >> 1) & 0x1f) |
		   (((ocp_addr >> 6) & 0x0f) << 8);
	return rtl83_smi_read(p, data_reg, val);
}

