{
	int ret;

	dev_info(dev, "transparent forwarding (CPU_CTRL=0x%04x, no tag)\n",
		 RTL_CPU_CTRL_TRANSPARENT);
	RTL_DO(rtl83_smi_write(p, RTL_REG_CPU_PORT_MASK, 0x0000));
	RTL_DO(rtl83_smi_write(p, RTL_REG_CPU_CTRL,
			       RTL_CPU_CTRL_TRANSPARENT));
	return 0;
}

/* idempotent gate: stock (e.g. HAB-closed TC8) left the chip fully
 * configured before chainloading us — detect that and skip the heavy
 * reload, preserving proven bench parity. */
static bool rtl83_already_up(struct rtl8363_priv *p)
{
	u16 cc = 0, f1 = 0;

	if (rtl83_smi_read(p, RTL_REG_CPU_CTRL, &cc))
		return false;
	if (rtl83_smi_read(p, RTL_REG_DIGITAL_INTF1_FORCE, &f1))
		return false;
	return cc == RTL_CPU_CTRL_TRANSPARENT &&
	       f1 == RTL_INTF1_FORCE_RGMII_1G;
}

static int rtl83_bringup(void *dev, struct rtl8363_priv *p)
{
	int ret;

	rtl83_chip_reset(dev, p);

	if (!p->has_reset && rtl83_already_up(p)) {
		dev_info(dev,
			 "switch already configured by prior stage — inheriting (skip heavy init)\n");
		/* re-assert transparent in case env perturbed it; cheap */
		return rtl83_set_transparent_mode(dev, p);
	}

	RTL_DO(rtl83_detect(dev, p));
	RTL_DO(rtl83_init_8367c(dev, p));
	RTL_DO(rtl83_port_admin_init(dev, p));
	RTL_DO(rtl83_setup_cpu_port(dev, p));
	RTL_DO(rtl83_setup_user_port(dev, p));
	RTL_DO(rtl83_set_transparent_mode(dev, p));
	dev_info(dev,
		 "rtl8363nbvb up: transparent L2, port6 RGMII 1G fixed\n");
	return 0;
}

/* ---- entry point: called from C60 board_late_init AFTER the FEC
 * (hence the "FEC" legacy mii bus) is up. NO DM device / NO DT
 * binding (those pulled in the DM-mdio->legacy bridge that
 * data-aborted in mdio_register's strcmp, wedging u-boot in SDP).
 * Idempotent: rtl83_already_up() inherit-skips a pre-configured
 * switch; a cold C60 chip gets the full 7-stage init. ---------- */
int rtl8363nbvb_bringup(void)
{
	struct rtl8363_priv pr = { .addr = 0x1d, .has_reset = false };

	return rtl83_bringup(NULL, &pr);
}
